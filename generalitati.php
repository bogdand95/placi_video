<h2 class="title">Componentele plăcii video</h2>
<div id="page" class="container">
<div class="entry">
<div id="box1">

	<p>O placă video modernă constă dintr-o placă de circuit imprimat pe care sunt dispuse (montate) componentele ce alcătuiesc dispozitivul responsabil pentru ieșirea semnalului video (placa grafică), componente după cum urmează:</p>
	<h3><b>Procesorul grafic (GPU - Graphics processing unit)</b></h3><br>
	<p>Procesorul grafic, GPU, este acel procesor dedicat, optimizat pentru accelerarea grafică, special conceput pentru a efectua calcule ȋn virgulă mobilă, calcule ce sunt fundamentale pentru redarea imaginilor 3D. Principalele atribute ale GPU-ului sunt frecvența nucleului, care variază de obicei de la 250 MHz la 4GHz și numarul de trasee (vertex și shaders (umbre)) care transpune o imagine 3D caracterizată de linii și coloane ȋntr-o imagine 2D, formată din pixeli.</p>
	<h3><b>BIOS-ul plăcii video</b></h3><br>
	<p>Bios-ul video sau firmware-ul reprezintă programul de bază, care este de obicei ascuns; acesta reglementează operațiunile plăcii video și oferă instrucțiuni care permit calculatorului și programului (software-ului) să interacționeze cu placa video. Este posibil să conțină informații cu privire la calendarul de memorie, viteze de operare și tensiuni de alimentare a procesorului grafic, memorie RAM, precum și alte informații.</p>
	<h3><b>Memoria video</b></h3><br>
	<p>Capacitatea de memorie a plăcilor video moderne variază de la 128 MB la 4GB, deși sunt foarte puține plăci cu memorii de peste 1 GB. Deoarece memoria video trebuie accesată de procesorul grafic și de circuitele de afișare, se folosesc adesea memorii de viteză mare sau memorii multi-port, cum ar fi VRAM, WRAM, SGRAM, etc. Ȋn preajma anului 2003 memoriile video s-au bazat pe tehnologia DDR. Astfel producătorii s-au orientat spre DDR2, GDDR3, GDDR4 și chiar GDDR5 utilizat ȋn special de ATI Radeon HD 4870. Rata de tact efectivă a memoriei ȋn plăcile moderne este, ȋn general, ȋntre 400 MHz și 3.8 GHz.</p>
	<p>Memoriea video mai poate fi utilizată pentru stocarea altor date, precum imaginea de pe ecran, Z-buffer (tamponul-memoria-Z), care gestionează adâncimea de coordonate ȋn grafica 3D, texturi, tampoanele vertex, precum şi programe compilate Shader.</p>
	
