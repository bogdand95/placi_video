	<h3><b>RAMDAC-ul</b></h3><br>
	<p>RAMDAC-ul (Random Access Memory Digital-to-Analog Converter – Convertor Digital-la-Analogic al Memoriei cu Acces Aleator), converteşte semnale digitale în semnale analogice pentru utilizarea de către un ecran de computer care utilizează intrări analogice, cum ar fi monitoarele CRT (Cathode Ray Tube – Tub Catodic). În funcţie de numărul de biţi utilizați şi rata de transfer de date RAMDAC, convertorul va fi în măsură să suporte diferite rate de refresh ale monitoarelor. Pentru monitoarele CRT, se recomandă lucrul la mai mult de 75 Hz şi niciodată sub 60 Hz, în scopul de a minimiza pâlpâirea; pentru monitoarele LCD (afișaj cu cristale lichide - liquid crystal display) acest inconveninet nu mai reprezintă o problemă). Toate LCD-urile actuale, monitoarele cu plasma si televizoarele moderne funcționeză ȋn domeniul digital şi nu necesită convertorul RAMDAC. Mai există câteva LCD-uri şi monitoare cu plasma care au doar intrări analogice (VGA, SCART, etc). Acestea necesită un RAMDAC, dar ele reconvertesc semnalul analogic înapoi la digital înainte de a-l afişa, cu pierderea inevitabilă de calitate care rezultă din această conversie digital-la-analogic-la-digital.</p>
