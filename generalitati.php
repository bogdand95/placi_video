	<br><h3><b>Răcirea plăcilor video</b></h3><br>
	<p>Plăcile video pot folosi multă energie electrică, care este transformată în căldură. În cazul în care căldura nu este disipată, placa video s-ar putea supraîncălzi, moment ȋn care intervine deteriorarea acesteia. Pentru protecția lor s-au construit dispozitive de răcire pentru a transfera căldură în altă parte. Există trei tipuri de răcire, utilizate în mod obişnuit pentru plăcile video:</p>
	<p><strong>Radiator:</strong> un radiator este un dispozitiv de răcire pasivă. Construit dintr-un metal conductor de căldură, de obicei aluminiu sau cupru, el preia căldura de la procesorul grafic al plăcii si o disipă.
	<p><strong>Ventilator de calculator:</strong> dispozitiv de răcire activă. Folosit ȋmpreună cu un radiator, vetilatorul are o eficiență mult mai mare la disiparea căldurii.</p>
	<p><strong>Răcire cu apă:</strong>dispozitiv de răcire cu apă compus dintr-un radiator special construit pentru utilizarea apei ca agent de răcire. Montat pe procesorul grafic, radiatorul are un gol ȋn interior prin care apa este pompată, aceasta preluând căldura. Apa este apoi răcită ȋn radiator. Acest tip de răcire este cea mai eficientă soluție, fără modificări extreme.</p>
